def _legacy_main() -> int:
    parser = argparse.ArgumentParser(description="ExplainX multi-scene pipeline refinement")
    parser.add_argument("topic", nargs="?", default="Planet Earth")
    parser.add_argument("--all", action="store_true", help="Run Earth, Photosynthesis, Human Heart")
    args = parser.parse_args()

    ffmpeg_ok = SubprocessFFmpegExecutor().is_available()
    if not ffmpeg_ok:
        print("WARNING: FFmpeg not found on PATH — videos will not be encoded.", flush=True)

    topics = ["Planet Earth", "Photosynthesis", "Human Heart"] if args.all else [args.topic]

    print("=" * 64)
    print("EXPLAINX PIPELINE REFINEMENT")
    print("=" * 64)

    results: list[TopicResult] = []
    for topic in topics:
        log: list[str] = []
        try:
            results.append(run_topic(topic, ffmpeg_ok=ffmpeg_ok, log=log))
        except Exception as exc:  # noqa: BLE001
            import traceback

            print(f"FAILED {topic}: {exc}")
            traceback.print_exc()
            r = TopicResult(topic)
            r.ok = False
            r.failures.append(str(exc))
            results.append(r)

    print("")
    print("=" * 64)
    print("VERIFICATION")
    print("=" * 64)
    all_ok = True
    for r in results:
        checks = {
            "multiple_scenes": len(r.scenes) >= 2,
            "multiple_assets": len(r.assets_used) >= 1 and r.diagram_count >= 2,
            "frames_rendered": r.frame_count > 0,
            "dynamic_duration": r.total_duration > 15.0,
            "playable_mp4": ffmpeg_ok and all(s.get("playable") for s in r.scenes) and bool(r.scenes),
            "no_failures": not r.failures,
        }
        ok = all(checks.values())
        all_ok = all_ok and ok
        print(f"\n{r.topic}: {'PASS' if ok else 'FAIL'}")
        for name, val in checks.items():
            print(f"  {'✓' if val else '✗'} {name}")
        for f in r.failures:
            print(f"    - {f}")

    print("")
    if all_ok and ffmpeg_ok:
        print("EXPLAINX PIPELINE VERIFIED")
        return 0
    print("EXPLAINX PIPELINE NOT VERIFIED — see checks above")
    return 1


